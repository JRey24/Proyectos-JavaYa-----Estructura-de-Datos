package parcial.estructuradedatoswhile;

public class Video4_WHILE 
{
    public static void main(String [] args)
    {
       int serie=11;
       int x=1;
       while (x<=25)
       {
           System.out.println(serie+" - ");
           serie=serie+11;
           x=x+1;
       }
    
    }
}

