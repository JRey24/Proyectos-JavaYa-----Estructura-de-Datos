package parcial.estructuradatos.forawhile;

public class Video3_FOR_a_WHILE 
{

    public static void main(String[] ar) 
    {
        int f = 1; 

        while (f <= 10) 
        {
            int tabla5 = f * 5;
            System.out.println(f + " * 5 = " + tabla5);
            f++;
        }
    }
}

