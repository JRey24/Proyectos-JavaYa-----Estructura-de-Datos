package parcial.Estructuradatos.whileafor;

public class Video4_WHILE_a_FOR 
{
        public static void main(String[] args) 
        {
        int serie = 11;
        for (int x = 1; x <= 25; x++) 
        {
            System.out.print(serie + " - ");
            serie = serie + 11;
        }
    }
}
